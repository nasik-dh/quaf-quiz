function sendQuizMenu(chatId) {
  const state = getUserState(chatId) || {};
  const completed = state.completedSubjects || [];

  if (!CONFIG.QUIZ_DATA || Object.keys(CONFIG.QUIZ_DATA).length === 0) {
    sendText(chatId, "โ No quizzes are currently available. Please check back later!");
    sendMainMenu(chatId);
    return;
  }

  const totalQuizzes = Object.keys(CONFIG.QUIZ_DATA).length;
  const completedCount = completed.length;

  // Show progress
  let menuText = `๐ <b>Quiz Center</b>\n\n`;
  menuText += `๐ Progress: ${completedCount}/${totalQuizzes} completed\n\n`;
  
  if (completedCount > 0) {
    menuText += `โ <b>Completed:</b>\n`;
    completed.forEach(quizKey => {
      const quiz = CONFIG.QUIZ_DATA[quizKey];
      if (quiz && state.quizScores && state.quizScores[quizKey]) {
        const score = state.quizScores[quizKey];
        menuText += `โข ${quiz.subjectId}: ${score.score}/${score.total} (${score.percent}%)\n`;
      }
    });
    menuText += `\n`;
  }

  const availableQuizzes = Object.entries(CONFIG.QUIZ_DATA).map(([key, quiz]) => {
    const isCompleted = completed.includes(key);
    const status = isCompleted ? "๐ Retake" : "๐ Start";
    return [{ text: `${status} ${quiz.subjectId}`, callback_data: key }];
  });

  if (completedCount >= totalQuizzes) {
    menuText += `๐ All quizzes completed! Use /restart to reset progress or retake any quiz:`;
  } else {
    menuText += `๐ Choose a quiz to continue:`;
  }

  // Add back button
  availableQuizzes.push([{ text: "๐ Back to Main Menu", callback_data: "back_to_menu" }]);

  const url = `https://api.telegram.org/bot${CONFIG.BOT_TOKEN}/sendMessage`;
  const keyboard = { inline_keyboard: availableQuizzes };

  const payload = {
    method: "post",
    contentType: "application/json",
    payload: JSON.stringify({
      chat_id: chatId,
      text: menuText,
      reply_markup: keyboard,
      parse_mode: "HTML"
    })
  };
  
  try {
    UrlFetchApp.fetch(url, payload);
  } catch (error) {
    console.error("Error sending quiz menu:", error);
  }
}

